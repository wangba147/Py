print("\n=== 7. 实战综合：项目文件统计器 ===")

from dataclasses import dataclass

@dataclass
class FileStats:
    """文件统计信息"""
    path: str
    total_lines: int
    code_lines: int
    blank_lines: int
    comment_lines: int

def analyze_cpp_file(filepath: Path) -> FileStats | None:
    """分析单个 C++ 文件的行数组成"""
    try:
        with open(filepath, encoding="utf-8", errors="replace") as f:
            lines = f.readlines()
    except (OSError, UnicodeDecodeError):
        return None

    code = blank = comment = 0
    for line in lines:
        stripped = line.strip()
        if not stripped:
            blank += 1
        elif stripped.startswith("//"):
            comment += 1
        elif stripped.startswith("/*"):
            comment += 1
        elif stripped.startswith("*"):
            comment += 1
        else:
            code += 1

    return FileStats(
        path=str(filepath),
        total_lines=len(lines),
        code_lines=code,
        blank_lines=blank,
        comment_lines=comment,
    )

