# 4. 异常处理
# ============================================================
# C++: try { ... } catch (const SomeException& e) { ... }
# Python: try/except/else/finally，四个子句！

print("\n=== 4. 异常处理 ===")

# --- 四个子句 ---
# else: 只有 try 成功时执行
# finally: 无论如何都执行（类似 C++ 的析构函数行为）
print("try/except/else/finally 演示:")

def risky_operation(filename, should_fail=False):
    """演示完整的异常处理"""
    result = None
    try:
        with open(filename, encoding="utf-8") as f:
            result = f.read()
        if should_fail:
            raise RuntimeError("模拟操作失败")
    except FileNotFoundError:
        print("  [except] 文件不存在")
    except PermissionError:
        print("  [except] 没有权限")
    except RuntimeError as e:
        print(f"  [except] 运行时错误: {e}")
    else:
        # C++ 没有 else！这里只有在 try 成功时才运行
        print(f"  [else] 读取成功: {len(result)} 字符")
    finally:
        # 类似 C++ 析构函数，无论如何都执行
        print(f"  [finally] 清理资源")

    return result

risky_operation(test_file)                # 成功
