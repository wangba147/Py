# 5. 自定义异常
# ============================================================
# C++: class MyError : public std::exception { ... };
#       throw MyError("msg");
# Python: class MyError(Exception): pass
#         raise MyError("msg")

print("\n=== 5. 自定义异常 ===")

# 一行定义异常类！
# C++ 需要写完整的类定义 + what() 方法
class BuildError(Exception):
    """所有构建相关异常的基类"""
    pass

class CompileError(BuildError):
    """编译阶段错误"""
    pass

class LinkError(BuildError):
    """链接阶段错误"""
    pass

# 异常层次的使用
def build_project(target: str, fail_at: str = "") -> str:
    """模拟构建过程"""
    if fail_at == "compile":
        raise CompileError(f"编译失败: {target}/main.cpp:42: error: undefined reference")
    if fail_at == "link":
        raise LinkError(f"链接失败: undefined symbol in {target}")
    return f"{target} 构建成功"

# 测试正常构建
print(f"正常构建: {build_project('MyApp')}")

# 捕获子类异常
for fail_type in ["compile", "link"]:
    try:
        build_project("MyApp", fail_at=fail_type)
    except CompileError as e:
        print(f"编译错误: {e}")
    except LinkError as e:
        print(f"链接错误: {e}")
    except BuildError as e:
        print(f"构建错误: {e}")  # 捕获所有构建错误

# --- raise from — 异常链 ---
# C++: std::throw_with_nested 概念类似但使用繁琐
print("\n异常链 (raise from):")
try:
    with open("nonexistent.json") as f:
        json.load(f)
except FileNotFoundError as e:
    # 包装成更语义化的错误
    try:
        raise RuntimeError("配置文件加载失败") from e
    except RuntimeError as wrapped:
        print(f"  原始错误: {wrapped.__cause__}")
        print(f"  包装错误: {wrapped}")
        # __cause__ 属性保存了原始异常


