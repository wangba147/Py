# 6. 上下文管理器
# ============================================================
# C++: RAII (构造函数获取资源，析构函数释放)
# Python: with 语句 + __enter__ / __exit__
#        或者 @contextmanager 装饰器

print("\n=== 6. 上下文管理器 ===")

from contextlib import contextmanager

# --- 方式1：基于类 ---
class Timer:
    """计时器上下文管理器"""
    def __enter__(self):
        import time
        self.start = time.perf_counter()
        return self  # 返回给 as 后面的变量

    def __exit__(self, exc_type, exc_val, exc_tb):
        import time
        elapsed = time.perf_counter() - self.start
        print(f"  类方式耗时: {elapsed:.6f}s")
        # 返回 False 表示不抑制异常

with Timer():
    total = sum(range(1000000))

# --- 方式2：基于生成器（更简洁！）---
@contextmanager
def timed_block(name: str = ""):
    """计时上下文管理器 — 基于装饰器"""
    import time
    label = f" [{name}]" if name else ""
    start = time.perf_counter()
    try:
        yield  # 这是 with 块的执行点
    finally:
        elapsed = time.perf_counter() - start
        print(f"  装饰器方式{label}耗时: {elapsed:.6f}s")

with timed_block("计算斐波那契"):
    a, b = 0, 1
    for _ in range(100000):
        a, b = b, a + b

# --- 实用上下文管理器：临时目录 ---
@contextmanager
def temp_working_dir(path: str):
    """临时切换工作目录，自动恢复（类似 pushd/popd）"""
    old = os.getcwd()
    os.chdir(path)
    try:
        yield
    finally:
        os.chdir(old)  # 无论如何都恢复

with temp_working_dir(tmp_dir):
    print(f"  切换到临时目录: {os.getcwd()}")
    # 在这里做事情...
print(f"  已恢复目录: {os.getcwd()}")

