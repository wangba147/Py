def demo_code_metrics():
    """演示：C++ 代码度量分析"""
    print("\n=== 2. 代码度量分析 ===")

    # 生成示例 C++ 文件进行分析
    sample_cpp = textwrap.dedent("""\
        #include <iostream>
        #include <vector>
        #include <string>
        #include <algorithm>

        // Utility function
        inline int add(int a, int b) { return a + b; }

        class Processor {
        private:
            std::vector<int> data_;
            std::string name_;
            int count_ = 0;
        public:
            Processor(const std::string& name) : name_(name) {}

            void addData(int value) {
                data_.push_back(value);
                count_++;
            }

            int process() {
                // TODO: implement complex processing
                int sum = 0;
                for (int v : data_) {
                    sum += v;
                }
                return sum;
            }

            std::string getName() const { return name_; }

            ~Processor() = default;
        };

        int main() {
            Processor p("test");
            p.addData(1);
            p.addData(2);
            std::cout << p.process() << std::endl;
            return 0;
        }
    """)

    # 写入临时文件
    tmp_dir = pathlib.Path(tempfile.mkdtemp())
    cpp_file = tmp_dir / "sample.cpp"
    cpp_file.write_text(sample_cpp, encoding="utf-8")

    # 统计函数数量 —— C++ 方式：grep + wc；Python 方式：正则直接匹配
    func_pattern = re.compile(
        r'(?:inline\s+)?(?:static\s+)?(?:virtual\s+)?'
        r'(?:void|int|float|double|string|auto|bool|size_t)\s+'
        r'(\w+)\s*\([^)]*\)\s*(?:const)?\s*\{',
        re.MULTILINE
    )
    functions = func_pattern.findall(sample_cpp)
    print(f"函数数量：{len(functions)} → {functions}")

    # 统计类数量
    class_pattern = re.compile(r'class\s+(\w+)\s*(?:final)?\s*\{', re.MULTILINE)
    classes = class_pattern.findall(sample_cpp)
    print(f"类数量：{len(classes)} → {classes}")

    # 统计 TODO/FIXME
    todo_pattern = re.compile(r'(TODO|FIXME|HACK|BUG)\b', re.IGNORECASE)
    todos = todo_pattern.findall(sample_cpp)
    print(f"TODO/FIXME 数量：{len(todos)} → {todos}")

    # 统计 include 数量
    include_pattern = re.compile(r'#include\s+[<"](\w+)[>"]', re.MULTILINE)
    includes = include_pattern.findall(sample_cpp)
    print(f"#include 数量：{len(includes)} → {includes}")

    # 计算圈复杂度（简化版：统计 if/else/for/while/switch/case）
    branch_pattern = re.compile(r'\b(if|else|for|while|switch|case|catch|&&|\|\|)\b')
    branches = len(branch_pattern.findall(sample_cpp))
    print(f"简化圈复杂度（分支数）：{branches}")

    # 行数统计
    lines = sample_cpp.split('\n')
    total_lines = len(lines)
    code_lines = len([l for l in lines if l.strip() and not l.strip().startswith('//')])
    comment_lines = len([l for l in lines if l.strip().startswith('//')])
    blank_lines = total_lines - code_lines - comment_lines
    print(f"总行数：{total_lines} | 代码行：{code_lines} | 注释行：{comment_lines} | 空行：{blank_lines}")

    shutil.rmtree(tmp_dir)


