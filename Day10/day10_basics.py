def demo_code_generator():
    """演示：从 JSON 规范生成 C++ 类"""
    print("\n=== 4. 代码生成器 ===")

    # JSON 规范定义数据模型
    spec = {
        "namespace": "app",
        "classes": [
            {
                "name": "User",
                "fields": [
                    {"name": "id", "type": "int", "default": "0"},
                    {"name": "name", "type": "std::string", "default": "\"\""},
                    {"name": "email", "type": "std::string", "default": "\"\""},
                    {"name": "active", "type": "bool", "default": "true"}
                ],
                "methods": ["toString"]
            },
            {
                "name": "Config",
                "fields": [
                    {"name": "port", "type": "int", "default": "8080"},
                    {"name": "host", "type": "std::string", "default": "\"localhost\""},
                    {"name": "verbose", "type": "bool", "default": "false"}
                ],
                "methods": ["toString", "validate"]
            }
        ]
    }

    for cls in spec["classes"]:
        print(f"\n--- 生成 {cls['name']} 类 ---")

        # 生成头文件
        header_lines = [
            f"#pragma once",
            f"#include <string>",
            f"#include <sstream>",
            f"",
            f"namespace {spec['namespace']} {{",
            f"",
            f"class {cls['name']} {{",
            f"private:",
        ]
        for f in cls["fields"]:
            header_lines.append(f"    {f['type']} {f['name']}_ = {f['default']};")

        header_lines.extend([
            f"public:",
            f"    // Constructors",
            f"    {cls['name']}() = default;",
        ])

        # 生成带参数的构造函数
        params = ", ".join(f"{f['type']} {f['name']}" for f in cls["fields"])
        init_list = ", ".join(f"{f['name']}_({f['name']})" for f in cls["fields"])
        header_lines.append(f"    {cls['name']}({params}) : {init_list} {{}}")

        # 生成 getter/setter
        for f in cls["fields"]:
            header_lines.append(f"")
            # getter —— C++ const 引用返回 vs Python @property
            if f["type"] == "std::string":
                header_lines.append(f"    const {f['type']}& {f['name']}() const {{ return {f['name']}_; }}")
            else:
                header_lines.append(f"    {f['type']} {f['name']}() const {{ return {f['name']}_; }}")
            # setter
            if f["type"] == "std::string":
                header_lines.append(f"    void set_{f['name']}(const {f['type']}& val) {{ {f['name']}_ = val; }}")
            else:
                header_lines.append(f"    void set_{f['name']}({f['type']} val) {{ {f['name']}_ = val; }}")

        # 生成指定方法
        if "toString" in cls["methods"]:
            header_lines.extend([
                f"",
                f"    std::string toString() const {{",
                f"        std::ostringstream oss;",
            ])
            for f in cls["fields"]:
                header_lines.append(f"        oss << \"{f['name']}=\" << {f['name']}_ << \";\";")
            header_lines.extend([
                f"        return oss.str();",
                f"    }}",
            ])

        if "validate" in cls["methods"]:
            header_lines.extend([
                f"",
                f"    bool validate() const {{",
                f"        return port_ > 0 && port_ < 65536 && !host_.empty();",
                f"    }}",
            ])

        header_lines.extend([
            f"}};",
            f"",
            f"}} // namespace {spec['namespace']}",
        ])

        header_content = "\n".join(header_lines)
        print(header_content)


