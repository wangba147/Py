# 3. 构建日志解析 —— 正则提取编译错误
# ============================================================
# C++ 对比：手动翻日志找错误；Python 自动解析+分类+汇总

def demo_build_log_parser():
    """演示：解析 C++ 构建日志"""
    print("\n=== 3. 构建日志解析 ===")

    # 模拟 gcc/clang 编译日志
    sample_log = textwrap.dedent("""\
        /home/user/project/src/main.cpp:15:10: error: 'cout' was not declared in this scope
        /home/user/project/src/utils.cpp:42:5: warning: unused variable 'x' [-Wunused-variable]
        /home/user/project/src/parser.cpp:88:20: error: no matching function for call to 'parse'
        /home/user/project/src/main.cpp:22:1: error: expected ';' after expression
        /home/user/project/src/io.cpp:7:3: warning: comparison between signed and unsigned [-Wsign-compare]
        /home/user/project/src/main.cpp:15:10: note: suggested alternative: 'std::cout'
        /home/user/project/include/types.h:5:2: error: 'string' does not name a type
        /home/user/project/src/utils.cpp:42:5: note: remove 'x' if unused
        /home/user/project/src/algo.cpp:100:8: warning: implicit conversion from 'float' to 'int' [-Wconversion]
        BUILD FAILED: 4 errors, 3 warnings
    """)

    # 正则提取 error/warning/note
    # C++ 方式：写复杂脚本或用 shell 管道 grep；Python 方式：正则一步到位
    error_re = re.compile(r'^(.+?):(\d+):(\d+): (error|warning|note): (.+)$', re.MULTILINE)

    errors = []
    warnings = []
    notes = []

    for m in error_re.finditer(sample_log):
        file_path, line, col, level, message = m.groups()
        entry = {"file": file_path, "line": int(line), "col": int(col), "message": message}
        if level == "error":
            errors.append(entry)
        elif level == "warning":
            warnings.append(entry)
        else:
            notes.append(entry)

    print(f"错误：{len(errors)} 个")
    for e in errors:
        print(f"  {e['file']}:{e['line']} → {e['message']}")

    print(f"警告：{len(warnings)} 个")
    for w in warnings:
        print(f"  {w['file']}:{w['line']} → {w['message']}")

    # 按文件分组统计 —— defaultdict 比 C++ unordered_map 更方便
    by_file = defaultdict(lambda: {"errors": 0, "warnings": 0})
    for e in errors:
        by_file[e["file"]]["errors"] += 1
    for w in warnings:
        by_file[w["file"]]["warnings"] += 1

    print("\n按文件统计：")
    for f, counts in sorted(by_file.items()):
        print(f"  {f}: {counts['errors']} errors, {counts['warnings']} warnings")


