# 1. 项目脚手架 —— Python 生成 C++ 项目结构
# ============================================================
# C++ 对比：C++ 项目需要手动创建目录、CMakeLists.txt、头文件等
# Python 可以一键生成标准项目骨架

def demo_project_scaffold():
    """演示：自动生成 C++ 项目骨架"""
    print("\n=== 1. 项目脚手架 ===")

    # C++ 方式（手动）：逐个 mkdir、touch、编写 CMakeLists.txt
    # Python 方式：用 pathlib + Template 一键生成

    project_name = "demo_cpp_project"
    project_dir = pathlib.Path(tempfile.mkdtemp()) / project_name

    # 定义项目模板
    cmake_template = Template(textwrap.dedent("""\
        cmake_minimum_required(VERSION 3.16)
        project($project_name VERSION $version LANGUAGES CXX)

        set(CMAKE_CXX_STANDARD $cxx_standard)
        set(CMAKE_CXX_STANDARD_REQUIRED ON)

        # Source files
        file(GLOB_RECURSE SOURCES "src/*.cpp")
        file(GLOB_RECURSE HEADERS "include/*.h")

        add_executable($project_name $${SOURCES} $${HEADERS})
        target_include_directories($project_name PRIVATE include)

        # Testing
        option(BUILD_TESTS "Build tests" ON)
        if(BUILD_TESTS)
            enable_testing()
            add_subdirectory(tests)
        endif()
    """))

    main_cpp_template = Template(textwrap.dedent("""\
        #include <iostream>
        #include "$header_name"

        int main() {
            std::cout << "$project_name v$version" << std::endl;
            auto result = $project_name::hello();
            std::cout << result << std::endl;
            return 0;
        }
    """))

    header_template = Template(textwrap.dedent("""\
        #pragma once
        #include <string>

        namespace $project_name {
            std::string hello();
        }
    """))

    source_template = Template(textwrap.dedent("""\
        #include "$header_name"

        namespace $project_name {
            std::string hello() {
                return "Hello from $project_name!";
            }
        }
    """))

    # 生成目录结构
    dirs = ["src", "include", "tests", "docs", "scripts"]
    for d in dirs:
        (project_dir / d).mkdir(parents=True, exist_ok=True)

    # 生成 CMakeLists.txt
    cmake_content = cmake_template.substitute(
        project_name=project_name,
        version="0.1.0",
        cxx_standard="17"
    )
    (project_dir / "CMakeLists.txt").write_text(cmake_content, encoding="utf-8")

    # 生成头文件
    header_name = f"{project_name}.h"
    header_content = header_template.substitute(
        project_name=project_name,
        header_name=header_name
    )
    (project_dir / "include" / header_name).write_text(header_content, encoding="utf-8")

    # 生成源文件
    source_content = source_template.substitute(
        project_name=project_name,
        header_name=header_name
    )
    (project_dir / "src" / f"{project_name}.cpp").write_text(source_content, encoding="utf-8")

    # 生成 main.cpp
    main_content = main_cpp_template.substitute(
        project_name=project_name,
        version="0.1.0",
        header_name=header_name
    )
    (project_dir / "src" / "main.cpp").write_text(main_content, encoding="utf-8")

    # 生成 README
    readme = f"# {project_name}\n\nVersion 0.1.0\n\nGenerated by Python toolchain.\n"
    (project_dir / "README.md").write_text(readme, encoding="utf-8")

    # 列出生成的文件
    print(f"项目骨架已生成：{project_dir}")
    for f in sorted(project_dir.rglob("*")):
        if f.is_file():
            rel = f.relative_to(project_dir)
            print(f"  {rel} ({f.stat().st_size} bytes)")

    # 清理临时目录
    shutil.rmtree(project_dir)
    print("(临时项目已清理)")


