# 6. 文件变更监控 —— 增量构建的基石
# ============================================================
# C++ 对比：CMake 用文件时间戳判断是否需要重新编译
# Python 用 hashlib 做内容哈希，更精确（不受 touch 影响）

def demo_file_change_tracking():
    """演示：文件内容哈希追踪变更"""
    print("\n=== 6. 文件变更监控 ===")

    # 生成临时测试文件
    tmp_dir = pathlib.Path(tempfile.mkdtemp())
    file1 = tmp_dir / "a.cpp"
    file2 = tmp_dir / "b.cpp"

    file1.write_text("int main() { return 0; }")
    file2.write_text("void foo() {}")

    # 计算内容哈希 —— C++ 方式：手动比较时间戳；Python：hashlib 内容哈希更可靠
    def file_hash(path):
        """计算文件 SHA256 哈希"""
        h = hashlib.sha256()
        h.update(path.read_bytes())
        return h.hexdigest()[:16]  # 取前16位足够判断变更

    # 第一次记录哈希
    hash_cache = {}
    for f in [file1, file2]:
        hash_cache[str(f)] = file_hash(f)
    print("初始哈希：")
    for p, h in hash_cache.items():
        print(f"  {pathlib.Path(p).name}: {h}")

    # 修改文件
    file1.write_text("int main() { return 42; }")  # 内容变了
    # file2 不变

    # 检测变更
    changed = []
    for f in [file1, file2]:
        current_hash = file_hash(f)
        old_hash = hash_cache[str(f)]
        if current_hash != old_hash:
            changed.append(f.name)
            print(f"  {f.name}: 变更检测！{old_hash} → {current_hash}")
        else:
            print(f"  {f.name}: 无变更（哈希一致）")

    print(f"\n需要重新编译的文件：{changed}")

    shutil.rmtree(tmp_dir)


