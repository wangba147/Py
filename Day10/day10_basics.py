def demo_test_discovery():
    """演示：测试自动发现与运行"""
    print("\n=== 8. 测试发现与运行 ===")

    # 生成模拟测试文件
    tmp_dir = pathlib.Path(tempfile.mkdtemp())
    test_dir = tmp_dir / "tests"
    test_dir.mkdir()

    # 模拟几个 C++ 测试文件名
    test_files = [
        "test_user.cpp",
        "test_config.cpp",
        "test_parser.cpp",
        "test_utils.cpp",
    ]
    for tf in test_files:
        (test_dir / tf).write_text(f"// {tf} - test file\n", encoding="utf-8")

    # 自动发现测试 —— C++ 方式：在 CMakeLists.txt 手动 add_test
    # Python 方式：glob + 正则模式自动发现
    discovered = list(test_dir.glob("test_*.cpp"))
    print(f"自动发现 {len(discovered)} 个测试：")
    for t in sorted(discovered):
        print(f"  {t.name}")

    # 模拟运行测试并收集结果
    # dataclass 比 C++ struct 更方便（自动生成 __init__ 等）
    @dataclass
    class TestResult:
        name: str
        passed: bool
        duration_ms: float
        output: str = ""

    # 模拟测试结果
    results = [
        TestResult("test_user", True, 12.5),
        TestResult("test_config", True, 8.3),
        TestResult("test_parser", False, 45.2, "Assertion failed: parse(\"{bad}\") returned null"),
        TestResult("test_utils", True, 3.1),
    ]

    passed = sum(1 for r in results if r.passed)
    failed = len(results) - passed
    total_time = sum(r.duration_ms for r in results)

    print(f"\n测试汇总：{passed} passed, {failed} failed, 总耗时 {total_time:.1f}ms")
    for r in results:
        status = "✅ PASS" if r.passed else "❌ FAIL"
        print(f"  {status} {r.name} ({r.duration_ms:.1f}ms)")
        if not r.passed:
            print(f"       → {r.output}")

    # 生成 JUnit XML 格式 —— C++ 项目 CI/CD 常用
    xml_lines = ['<testsuite tests="4" failures="1" time="{:.1f}">'.format(total_time)]
    for r in results:
        xml_lines.append(f'  <testcase classname="{spec.get("namespace", "app")}" name="{r.name}" time="{r.duration_ms/1000:.3f}">')
        if not r.passed:
            xml_lines.append(f'    <failure message="{r.output}"/>')
        xml_lines.append(f'  </testcase>')
    xml_lines.append('</testsuite>')
    print("\nJUnit XML 格式（CI/CD 集成用）：")
    print("\n".join(xml_lines))

    shutil.rmtree(tmp_dir)


