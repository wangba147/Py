# 7. 性能计时器 —— Python vs C++ 基准对比
# ============================================================
# C++ 对比：<chrono> 高精度计时；Python time.perf_counter 更简洁

def demo_performance_timer():
    """演示：性能计时与基准测试"""
    print("\n=== 7. 性能计时器 ===")

    # C++ 方式：
    # auto start = std::chrono::high_resolution_clock::now();
    # ... code ...
    # auto end = std::chrono::high_resolution_clock::now();
    # auto ms = std::chrono::duration_cast<std::chrono::milliseconds>(end - start).count();

    # Python 方式：更简洁，用 contextmanager 封装
    @contextmanager
    def timer(name):
        start = time.perf_counter()
        yield
        elapsed = time.perf_counter() - start
        print(f"  [{name}] 耗时 {elapsed:.4f}s ({elapsed*1000:.2f}ms)")

    # 对比不同算法性能
    data = list(range(10000))

    with timer("列表求和 - sum()"):
        total = sum(data)

    with timer("列表求和 - 手动循环"):
        total2 = 0
        for x in data:
            total2 += x

    with timer("列表求和 - reduce"):
        from functools import reduce
        total3 = reduce(lambda a, b: a + b, data)

    assert total == total2 == total3, "结果不一致"
    print(f"  三种方式结果一致：{total}")


